#include "mini_mathlib.h"

signed short int min0(signed short int first, signed short int second)
{
     if (first < second) 
          return first;
     else
          return second;
}
     
signed short int max0(signed short int first, signed short int second)
{
     if (first > second) 
          return first;
     else
          return second;
}
     
